#include "libft.h"

void	ft_strclr(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return ;
	while (s[i] != '\0')
	{	
		s[i] = 0;
		i++;
	}
	s[i] = '\0';
	return ;
}
